#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int ac, char **av)
{
	int	k;

	k = 1;
	while (k < ac)
	{
		ft_putstr(av[k]);
		write(1, "\n", 1);
		k++;
	}
}
